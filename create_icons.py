#!/usr/bin/env python3
import os

# Create icons directory
os.makedirs('icons', exist_ok=True)

# Minimal valid PNG data (1x1 transparent pixel) - works for all sizes
minimal_png = bytes([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D,  # IHDR chunk length
    0x49, 0x48, 0x44, 0x52,  # IHDR
    0x00, 0x00, 0x00, 0x10,  # Width: 16
    0x00, 0x00, 0x00, 0x10,  # Height: 16
    0x08, 0x02, 0x00, 0x00, 0x00,  # Bit depth, color type, etc.
    0x90, 0x91, 0x68, 0x36,  # CRC
    0x00, 0x00, 0x00, 0x0C,  # IDAT chunk length
    0x49, 0x44, 0x41, 0x54,  # IDAT
    0x08, 0x99, 0x01, 0x01, 0x00, 0x01, 0x00, 0xFE, 0xFF, 0x00, 0x00, 0x00,  # Compressed data
    0x02, 0x00, 0x01,  # CRC
    0x00, 0x00, 0x00, 0x00,  # IEND chunk length
    0x49, 0x45, 0x4E, 0x44,  # IEND
    0xAE, 0x42, 0x60, 0x82   # CRC
])

# Create all three icon sizes with the same data
for size in ['16', '48', '128']:
    icon_path = f'icons/icon-{size}.png'
    with open(icon_path, 'wb') as f:
        f.write(minimal_png)
    print(f"Created {icon_path}")

print("\nAll PNG icon files created successfully!")
